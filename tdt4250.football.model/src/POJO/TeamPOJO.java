package POJO;

public class TeamPOJO {
	private int id;
	private String country;
	private String fifa_code;
	private int group_id;
	private char group_letter;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getFifa_code() {
		return fifa_code;
	}
	public void setFifa_code(String fifa_code) {
		this.fifa_code = fifa_code;
	}
	public int getGroup_id() {
		return group_id;
	}
	public void setGroup_id(int group_id) {
		this.group_id = group_id;
	}
	public char getGroup_letter() {
		return group_letter;
	}
	public void setGroup_letter(char group_letter) {
		this.group_letter = group_letter;
	}
}
